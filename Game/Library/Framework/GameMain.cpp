#include "GameMain.h"
#include "..\..\Game\Scene\SceneManager.h"

//==========================================================//
//*内　容	コンストラクタ
//*引　数	なし
//*戻り値	なし
//==========================================================//
GameMain::GameMain(int width, int height) :Game(width, height)
{
	m_game = new Game(width, height);
	m_sceneManager = new SceneManager(width, height);
}

//==========================================================//
//*内　容	初期化
//*引　数	なし
//*戻り値	なし
//==========================================================//
int GameMain::Initialize()
{
	m_game->Initialize();
	m_sceneManager->m_scene->InitializeGame();
	return 0;
}

//==========================================================//
//*内　容	画面サイズ取得
//*引　数	なし
//*戻り値	なし
//==========================================================//
void GameMain::GetDefaultSize(int & width, int & height)
{
	m_game->GetDefaultSize(width, height);
}

//==========================================================//
//*内　容	更新や描画の作動
//*引　数	なし
//*戻り値	なし
//==========================================================//
void GameMain::Tick()
{
	//	更新
	m_game->Run();
	this->Update();
	//	描画
	m_game->Clear();
	m_game->Render();
	this->Render();
	m_game->Present();
}

//==========================================================//
//*内　容	更新
//*引　数	なし
//*戻り値	なし
//==========================================================//
void GameMain::Update()
{
	m_sceneManager->Scene();
}

//==========================================================//
//*内　容	描画
//*引　数	なし
//*戻り値	なし
//==========================================================//
void GameMain::Render()
{
	m_sceneManager->m_scene->RenderGame();
}
